#!/usr/bin/env node
// scripts/gen-repo-tree.mjs
// Generates docs/repo-tree.md — a flat, sorted manifest of every git-tracked
// file. The mechanical, always-current source of truth for repo layout;
// companion to the hand-authored docs/codebase-reference.md (per-file
// annotations) and architecture.md (systems view).
//
// Regenerate whenever a delta adds / moves / deletes a file:
//     npm run gen-tree
//
// DO NOT hand-edit the output — the next regen overwrites it.

import { execSync } from "node:child_process";
import { writeFileSync, mkdirSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, join } from "node:path";

const repoRoot = join(dirname(fileURLToPath(import.meta.url)), "..");
const outPath = join(repoRoot, "docs", "repo-tree.md");

// `git ls-files` is already sorted and tracked-only: no node_modules, no build
// output, nothing gitignored. Run it from the repo root so paths are
// repo-relative. If it ever surfaces something unexpected (e.g. supabase/.temp
// scratch state), that's a real "should this be committed?" signal, not noise.
let files;
try {
  files = execSync("git ls-files", { cwd: repoRoot, encoding: "utf8" })
    .split("\n")
    .map((line) => line.trim())
    .filter(Boolean);
} catch (err) {
  console.error("gen-repo-tree: `git ls-files` failed — run inside the repo.");
  console.error(err.message);
  process.exit(1);
}

const out = [
  "# SquirreLingo — Repo file manifest (generated)",
  "",
  "> Auto-generated by `npm run gen-tree` from `git ls-files`. DO NOT hand-edit —",
  "> the next regen overwrites it. Tracked files only: no `node_modules`, build",
  "> output, or gitignored files. Pair with `docs/codebase-reference.md` for",
  "> per-file annotations; this file is the mechanical, always-current index.",
  ">",
  `> ${files.length} tracked files.`,
  "",
  "```",
  ...files,
  "```",
  "",
].join("\n");

mkdirSync(dirname(outPath), { recursive: true });
writeFileSync(outPath, out);
console.log(`gen-repo-tree: wrote ${outPath} (${files.length} files)`);
